

namespace Curso {

class Retangulo
{
	private double larg, alt;

          
	public void SetLargura(double larg)
    {
		this->larg = larg;
	}

	public double GetLargura()
	{
		return larg;    
	}

	public void SetAltura(double alt)
	{
		this->alt = alt;
	}

	public double GetAltura()
	{
		return alt;
	}

}
}
